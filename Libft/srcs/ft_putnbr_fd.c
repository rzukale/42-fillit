#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long i;

	i = nb;
	if (i < 0)
	{
		ft_putchar_fd('-', fd);
		i *= -1;
	}
	if ((i / 10) > 0)
		ft_putnbr_fd(i / 10, fd);
	ft_putchar_fd(i % 10 + '0', fd);
}

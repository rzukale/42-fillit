#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && len-- > 0)
		dest[i++] = src[j++];
	dest[i] = '\0';
	return (dest);
}
